import math

print("Subtraction with python: 10 - 5")
print(10 - 5)

print("\nAddition with python: 10 + 5")
print(10 + 5)

print("\nMultiplication with python: 10 * 5")
print(10 * 5)

print("\nDivision with python: 10 / 3")
print(10 / 3)

print("\nFloor division with python: 10 // 3")
print(10 // 3)

print("\nModulus(remainder) with python: 10 % 3")
print(10 % 3)

print("\nPower of with python: 3 ** 2")
print(3 ** 2)

print("\nPower of with python: 3 ** 3")
print(3 ** 3)

print("\nPower of with python pow operator: pow(3, 3)")
print(pow(3, 3))

print("\nSquare root of with python: math.sqrt(9)")
print(math.sqrt(9))

print("\nSquare root of with python: math.cbrt(27)")
print(math.cbrt(27))

print("\nAbsolute value of with python: abs(-27)")
print(abs(-27))
