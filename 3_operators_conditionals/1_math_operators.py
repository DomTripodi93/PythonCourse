import math

print("\nSubtraction: 5 - 3")
print(5 - 3)

print("\nAddition: 1 + 2")
print(1 + 2)

print("\nMultiplication: 5 * 5")
print(5 * 5)

print("\nDivision: 25 / 5")
print(25 / 5)

print("\nFloor Division(Rounded Down): 10 // 3")
print(10 // 3)

print("\nModulus(Remainder): 10 % 3")
print(10 % 3)

print("\nSquared: 5 ** 2")
print(5 ** 2)

print("\nPower: pow(5, 3)")
print(pow(5, 3))

print("\nSquare Root: math.sqrt(25)")
print(math.sqrt(25))

print("\nCube Root: math.cbrt(125)")
print(math.cbrt(125))

print("\nAbsolute Value: abs(-25)")
print(abs(-25))
